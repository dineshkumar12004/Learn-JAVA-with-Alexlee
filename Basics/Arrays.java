package Basics;

public class Arrays {
    public static void main(String[] args) {
        System.out.println("Learning Arrays  in JAVA");

        String backpack [] = {"pencil", "rubber", "bottle"};
        String lunchbag [] = {"towel","fork","tissues"};

        System.out.println(lunchbag[0]);
        System.out.println(lunchbag[1]);
        System.out.println(backpack[2]);
    }
}
