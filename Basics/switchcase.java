package Basics;

public class switchcase {
  
    
    public static void main(String[] args) {
        System.out.println("hello !");

        System.out.println("Switch cases in JAVA");

        int day = 3;

    switch (day) {
        case 1:
            System.out.println("Case 1 is executed");
            break;
        case 2:
            System.out.println();
            break;
        case 3:
            System.out.println(" case  3 is executed !!!");
            break;

    }

    }
}
