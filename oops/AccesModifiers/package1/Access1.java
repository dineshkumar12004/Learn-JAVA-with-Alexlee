package package1;

public class Access1 {

    // default access modifiers
    int hour = 3;
    int minute = 30;

    // public access modifiers
    public int hour = 3;
    public int minute = 30;

    // private access modifiers
    private int hour = 3;
    private int minute = 30;

    // these only have access inside Access1 class

    // protected access modifiers
    protected int hour = 3;
    protected int minute = 30;

    // It gives access inside the package alone

}
